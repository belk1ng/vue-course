import { defineComponent, ref, watch } from 'vue'

export default defineComponent({
  name: 'MapApp',

  setup() {
    // –†–µ–∞–∫—Ç–∏–≤–Ω—ã–µ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç –º–µ—Ç–∫–∏
    let x = ref(0)
    let y = ref(0)

    // –†–µ–∞–∫—Ç–∏–≤–Ω–∞—è –ø–µ—Ä–µ–º–µ–Ω–Ω–∞—è –¥–ª—è –¥–æ—Å—Ç—É–ø–∞ –¥–æ –Ω–æ–¥—ã –º–∞—Ä–∫–µ—Ä–∞
    const pin = ref(null)

    /**
     * –û–±—Ä–∞–±–æ—Ç—á–∏–∫ –∫–ª–∏–∫–∞ –ø–æ –∫–∞—Ä—Ç–µ –¥–ª—è —É—Å—Ç–∞–Ω–æ–≤–∫–∏ –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç –º–µ—Ç–∫–∏
     * @param {MouseEvent} event
     */
    function handleClick(event) {
      x.value = event.offsetX
      y.value = event.offsetY
    }

    // –°–ª–µ–¥–∏–º –∑–∞ X –∏ Y –¥–ª—è —É—Å—Ç–∞–Ω–æ–≤–∫–∏ –Ω–æ–≤–æ–≥–æ –ø–æ–ª–æ–∂–µ–Ω–∏—è
    watch([x, y], ([newX, newY]) => {
      // –ù–∞—Ö–æ–¥–∏–º –º–µ—Ç–∫—É –∏ –∏–∑–º–µ–Ω—è–µ–º –µ—ë –ø–æ–ª–æ–∂–µ–Ω–∏–µ
      const pinNode = pin.value
      if (pinNode) {
        pinNode.style.left = `${newX}px`
        pinNode.style.top = `${newY}px`
      }
    })

    return {
      handleClick,
      pin,
    }
  },

  template: `
    <div class="map" @click="handleClick">
      <img class="map-image" src="./map.png" alt="Map" draggable="false" />
      <span ref="pin" class="pin">üìç</span>
    </div>
  `,
})
